package com.example.deepJava.wrapper;

public class AutoBoxingAutoUnboxingTest {
    /**
     *  Autoboxing is a process of converting primitive types to their wrapper classes.
     *  For example, the primitive type int is converted to the wrapper class Integer.
     */
    Integer x = 10;

    /**
     * Autounboxing is a process of converting wrapper classes to their primitive types.
     * For example, the wrapper class Integer is converted to the primitive type int.
     */
     int y = x;

}
