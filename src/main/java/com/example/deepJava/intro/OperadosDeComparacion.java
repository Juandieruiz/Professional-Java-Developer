package com.example.deepJava.intro;

public class OperadosDeComparacion {
    public static void main(String[] args) {
        double sueldo1= 18000.0;
        double sueldo2= 14000.0;

        System.out.println(sueldo1 > sueldo2);
        System.out.println(sueldo1< sueldo2);
        System.out.println(sueldo1>=sueldo2);
        System.out.println(sueldo1<=sueldo2);
        System.out.println(sueldo1==sueldo2);
        System.out.println(sueldo1!=sueldo2);
    }

}

